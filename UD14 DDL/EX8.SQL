create database Vuelos;
use Vuelos;
  CREATE TABLE IF NOT EXISTS tarjetaEmbarque(
IdCodeEmbarque INTEGER NOT NULL PRIMARY KEY
);
CREATE TABLE IF NOT EXISTS clientes(
IdCodeCliente INTEGER AUTO_INCREMENT NOT NULL PRIMARY KEY,
Dni VARCHAR(45) NOT NULL,
nombre VARCHAR(45) NOT NULL,
apellido VARCHAR(45) NOT NULL,
direccion VARCHAR(45) NOT NULL,
telefono INTEGER NOT NULL,
NumTarjetaCredito INTEGER NOT NULL,
IdCodeEmbarque INT NOT NULL  REFERENCES tarjetaEmbarque (IdCodeEmbarque) ON DELETE CASCADE ON UPDATE CASCADE
);
CREATE TABLE IF NOT EXISTS reservas(
IdCodeReserva INTEGER AUTO_INCREMENT NOT NULL PRIMARY KEY,
NumAsientos SMALLINT NOT NULL,
Fecha DATETIME NOT NULL,
IdCodeCliente INTEGER NOT NULL,
IdCodeEmbarque INTEGER NOT NULL,
FOREIGN KEY (IdCodeCliente) REFERENCES clientes (IdCodeCliente) ON DELETE CASCADE ON UPDATE CASCADE,
FOREIGN KEY (IdCodeEmbarque) REFERENCES tarjetaEmbarque (IdCodeEmbarque) ON DELETE CASCADE ON UPDATE CASCADE
);
CREATE TABLE IF NOT EXISTS asiento(
IdAsiento INTEGER NOT NULL PRIMARY KEY,
fila INTEGER NOT NULL,
columna VARCHAR(2) NOT NULL,
planta INTEGER NOT NULL,
IdCodeEmbarque INT NOT NULL  REFERENCES tarjetaEmbarque (IdCodeEmbarque) ON DELETE CASCADE ON UPDATE CASCADE
);
CREATE TABLE IF NOT EXISTS aeropuerto(
IdCodeAeropuerto INTEGER NOT NULL PRIMARY KEY,
nombre VARCHAR(45) NOT NULL,
localidad VARCHAR(45) NOT NULL,
pais VARCHAR(45) NOT NULL
);
CREATE TABLE IF NOT EXISTS vuelo(
IdCodeVuelo INTEGER UNSIGNED NOT NULL PRIMARY KEY,
fechaSalida DATETIME NOT NULL,
fechaLlegada DATETIME NOT NULL,
aeropuertoSalida VARCHAR(45) NOT NULL,
aeropuertoLlegada VARCHAR(45) NOT NULL,
IdCodeAeropuerto INTEGER NOT NULL,
FOREIGN KEY (IdCodeAeropuerto) REFERENCES aeropuerto (IdCodeAeropuerto) ON DELETE CASCADE ON UPDATE CASCADE
);
CREATE TABLE IF NOT EXISTS avion(
IdCodeAvion INTEGER NOT NULL PRIMARY KEY,
NumPlazas INTEGER NOT NULL,
IdCodeVuelo INT NOT NULL  REFERENCES vuelo (IdCodeVuelo) ON DELETE CASCADE ON UPDATE CASCADE
);